    модифицировано Alex Keda, для VPS */
/******************************************************************************/
/******************************************************************************/
$__args_list = Array('IsCreate','Folder');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
if(Is_Error(System_Load('libs/Artichow.php')))
  return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
$Result = Array('Title'=>'Распределение доходов по серверам VPS');
#-------------------------------------------------------------------------------
if(!$IsCreate)
  return $Result;
#-------------------------------------------------------------------------------
$NoBody = new Tag('NOBODY');
#-------------------------------------------------------------------------------
$NoBody->AddChild(new Tag('P','Данный вид статистики содержит информацию о доходности каждого из имеющихся серверов VPS за 1 мес.'));
$NoBody->AddChild(new Tag('P','Суммируются цены за месяц тарифов всех активных заказов размещенных на сервере.'));
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Tables = Array('VPSServers','VPSOrders','VPSSchemes');
$Columns = Array('SUM(`CostDay`*`MinDaysPay`) as `Income`','Address');
$Condition = Array(
			'Where'	 =>Array(
					'`VPSSchemes`.`ID` = `VPSOrders`.`SchemeID`',
					'`VPSServers`.`ID` = `VPSOrders`.`ServerID`',
					'`VPSOrders`.`StatusID`="Active"'
					),
			'GroupBy'=>'ServerID',
			'SortOn' =>'Address'
		);
#-------------------------------------------------------------------------------
$Incomes = DB_Select($Tables,$Columns,$Condition);
#-------------------------------------------------------------------------------
switch(ValueOf($Incomes)){
  case 'error':
    return ERROR | @Trigger_Error(500);
  case 'exception':
    return $Result;
  case 'array':
   #----------------------------------------------------------------------------
   $Balance = 0;
   #----------------------------------------------------------------------------
   $Params = $Labels = Array();
   #----------------------------------------------------------------------------
   $Table = Array(Array(new Tag('TD',Array('class'=>'Head'),'Адрес сервера'),new Tag('TD',Array('class'=>'Head'),'Доход')));
   #----------------------------------------------------------------------------
   foreach($Incomes as $Income){
     #--------------------------------------------------------------------------
     $Balance += $Income['Income'];
     #--------------------------------------------------------------------------
     $Params[] = $Income['Income'];
     $Labels[] = $Income['Address'];
     #--------------------------------------------------------------------------
     $Summ = Comp_Load('Formats/Currency',$Income['Income']);
     if(Is_Error($Summ))
       return ERROR | @Trigger_Error(500);
     #--------------------------------------------------------------------------
     $Table[] = Array($Income['Address'],$Summ);
   }
   #----------------------------------------------------------------------------
   $Comp = Comp_Load('Formats/Currency',$Balance);
   if(Is_Error($Comp))
     return ERROR | @Trigger_Error(500);
   #----------------------------------------------------------------------------
   $Table[] = Array(new Tag('TD',Array('colspan'=>3,'class'=>'Standard','align'=>'right'),SPrintF('Общий доход от серверов: %s',$Comp)));
   #----------------------------------------------------------------------------
   $Comp = Comp_Load('Tables/Extended',$Table);
   if(Is_Error($Comp))
     return ERROR | @Trigger_Error(500);
   #----------------------------------------------------------------------------
   $NoBody->AddChild($Comp);
   #----------------------------------------------------------------------------
   if(Count($Params) > 1){
     #-------------------------------------------------------------------------
     $File = SPrintF('%s.jpg',Md5('VPSIncome1'));
     #-------------------------------------------------------------------------
     Artichow_Pie('Распределение доходов по серверам VPS',SPrintF('%s/%s',$Folder,$File),$Params,$Labels);
     #-------------------------------------------------------------------------
     $NoBody->AddChild(new Tag('BR'));
     #-------------------------------------------------------------------------
     $NoBody->AddChild(new Tag('IMG',Array('src'=>$File)));
   }
  break;
  default:
    return ERROR | @Trigger_Error(101);
}
#-------------------------------------------------------------------------------
$Result['DOM'] = $NoBody;
#-------------------------------------------------------------------------------
return $Result;

?>
