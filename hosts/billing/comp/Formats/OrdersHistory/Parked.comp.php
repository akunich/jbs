/******************************************************************************/
/******************************************************************************/
$__args_list = Array('Parked','Length');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
$Parked = ($Parked?Explode(',',$Parked):Array());
#-------------------------------------------------------------------------------
$Parked = Array_Unique($Parked);
#-------------------------------------------------------------------------------
$Count = Count($Parked);
#-------------------------------------------------------------------------------
if($Count > 30)
	$Parked = Array_Slice($Parked,0,29);
#-------------------------------------------------------------------------------
$Domain = $Text = Current($Parked);
#-------------------------------------------------------------------------------
// а бывает что и нет домена-то
if(!$Text)
	$Text = '-';
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
if(Mb_StrLen($Text) > $Length)
	$Text = SPrintF('%s...',Mb_SubStr($Text,0,$Length));
#-------------------------------------------------------------------------------
$A = new Tag('A',Array('target'=>'blank','href'=>SPrintF('http://%s/',$Domain)),$Text);
#-------------------------------------------------------------------------------
$LinkID = UniqID('Parked');
#-------------------------------------------------------------------------------
$Links = &Links();
# Коллекция ссылок
$Links[$LinkID] = &$A;
#-------------------------------------------------------------------------------
$Comp = Comp_Load('Form/Prompt',$LinkID,SPrintF('<B>Всего: %u</B><BR />%s',$Count,Implode('<BR />',$Parked)));
if(Is_Error($Comp))
	return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
UnSet($Links[$LinkID]);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return $A;
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------

?>
