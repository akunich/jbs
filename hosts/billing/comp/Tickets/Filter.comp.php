/******************************************************************************/
/******************************************************************************/
$__args_list = Array('LinkID');
/******************************************************************************/
/******************************************************************************/
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
$Links = &Links();
# Коллекция ссылок
$Template = &$Links[$LinkID];
/******************************************************************************/
/******************************************************************************/
$Tr = new Tag('TR');
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Groups = DB_Select('Groups',Array('ID','Name'));
#-------------------------------------------------------------------------------
switch(ValueOf($Groups)){
case 'error':
	return ERROR | @Trigger_Error(500);
case 'exception':
	# No more...
	break;
case 'array':
	#-------------------------------------------------------------------------------
	$Options = Array();
	#-------------------------------------------------------------------------------
	$Options['Default'] = 'Любая';
	#-------------------------------------------------------------------------------
	foreach($Groups as $Group)
		$Options[$Group['ID']] = $Group['Name'];
	#-------------------------------------------------------------------------------
	$GroupOnlyID = 'Default';
	#-------------------------------------------------------------------------------
	$Session = &$Template['Session'];
	#-------------------------------------------------------------------------------
	if(IsSet($Session['GroupOnlyID']))
		$GroupOnlyID = $Session['GroupOnlyID'];
	#-------------------------------------------------------------------------------
	$Args = Args();
	#-------------------------------------------------------------------------------
	if(IsSet($Args['GroupOnlyID']))
		$GroupOnlyID = $Args['GroupOnlyID'];
	#-------------------------------------------------------------------------------
	$Session['GroupOnlyID'] = $GroupOnlyID;
	#-------------------------------------------------------------------------------
	$AddingWhere = &$Template['Source']['Adding']['Where'];
	#-------------------------------------------------------------------------------
	if($GroupOnlyID != 'Default')
		$AddingWhere[] = SPrintF('`TargetGroupID` = %u',$GroupOnlyID);
	#-------------------------------------------------------------------------------
	$Comp = Comp_Load('Form/Select',Array('name'=>'GroupOnlyID','onchange'=>'TableSuperReload();'),$Options,$GroupOnlyID);
	if(Is_Error($Comp))
		return ERROR | @Trigger_Error(500);
	#-------------------------------------------------------------------------------
	$Tr->AddChild(new Tag('NOBODY',new Tag('TD',Array('class'=>'Comment'),'Только для группы'),new Tag('TD',$Comp)));
	#-------------------------------------------------------------------------------
	break;
	#-------------------------------------------------------------------------------
default:
	return ERROR | @Trigger_Error(101);
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Groups = DB_Select('Groups',Array('ID','Name'));
#-------------------------------------------------------------------------------
switch(ValueOf($Groups)){
case 'error':
	return ERROR | @Trigger_Error(500);
case 'exception':
	# No more...
	break;
case 'array':
	#-------------------------------------------------------------------------------
	$Options = Array();
	#-------------------------------------------------------------------------------
	$Options['Default'] = 'Любая';
	#-------------------------------------------------------------------------------
	foreach($Groups as $Group)
		$Options[$Group['ID']] = $Group['Name'];
	#-------------------------------------------------------------------------------
	$GroupExcludeID = 'Default';
	#-------------------------------------------------------------------------------
	$Session = &$Template['Session'];
	#-------------------------------------------------------------------------------
	if(IsSet($Session['GroupExcludeID']))
		$GroupExcludeID = $Session['GroupExcludeID'];
	#-------------------------------------------------------------------------------
	$Args = Args();
	#-------------------------------------------------------------------------------
	if(IsSet($Args['GroupExcludeID']))
		$GroupExcludeID = $Args['GroupExcludeID'];
	#-------------------------------------------------------------------------------
	$Session['GroupExcludeID'] = $GroupExcludeID;
	#-------------------------------------------------------------------------------
	$AddingWhere = &$Template['Source']['Adding']['Where'];
	#-------------------------------------------------------------------------------
	if($GroupExcludeID != 'Default')
		$AddingWhere[] = SPrintF('`TargetGroupID` != %u',$GroupExcludeID);
	#-------------------------------------------------------------------------------
	$Comp = Comp_Load('Form/Select',Array('name'=>'GroupExcludeID','onchange'=>'TableSuperReload();'),$Options,$GroupExcludeID);
	if(Is_Error($Comp))
		return ERROR | @Trigger_Error(500);
	#-------------------------------------------------------------------------------
	$Tr->AddChild(new Tag('NOBODY',new Tag('TD',Array('class'=>'Comment'),'Исключая группу'),new Tag('TD',$Comp)));
	#-------------------------------------------------------------------------------
	break;
	#-------------------------------------------------------------------------------
default:
	return ERROR | @Trigger_Error(101);
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
if(!Count($Tr->Childs))
	return FALSE;
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return new Tag('TABLE',Array('class'=>'Standard','cellspacing'=>5),$Tr);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------

?>
