/******************************************************************************/
/******************************************************************************/
$__args_list = Array('ContractID','Columns');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
if(!$Columns)
	$Columns = Array('*');
#-------------------------------------------------------------------------------
if(!Is_Array($Columns))
	$Columns = Array($Columns);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Contract = DB_Select('Contracts',$Columns,Array('UNIQ','ID'=>$ContractID));
#---------------------------------------------------------------------------
switch(ValueOf($Contract)){
case 'error':
	return ERROR | @Trigger_Error(500);
case 'exception':
	return new gException('CONTRACT_NOT_FOUND','Договор не найден');
case 'array':
	break;
default:
	return ERROR | @Trigger_Error(101);
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return $Contract;
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------

?>
