$Where = SPrintF("`IsProtected` = 'yes' AND `ID` = %u",$ClausesGroups['ID']);
$Count = DB_Count('ClausesGroups',Array('Where'=>$Where));
#-------------------------------------------------------------------------------
if(Is_Error($Count))
	return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
if($Count)
	return new gException('CLAUSE_GROUP_IS_PROTECTED','Это защищённая группа статей, её нельзя удалить');
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Where = SPrintF("`GroupID` = %u",$ClausesGroups['ID']);
$Count = DB_Count('Clauses',Array('Where'=>$Where));
#-------------------------------------------------------------------------------
if(Is_Error($Count))
	return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
if($Count)
	return new gException('CLAUSE_GROUP_HAVE_CLAUSES','В группе статей есть статьи, её нельзя удалить');
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return TRUE;
#-------------------------------------------------------------------------------

