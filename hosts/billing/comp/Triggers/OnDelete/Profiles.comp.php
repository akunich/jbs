/******************************************************************************/
/******************************************************************************/
$__args_list = Array('Profile');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
if(Is_Error(System_Load('libs/Upload.php')))
	return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Count = DB_Count('Contracts',Array('Where'=>SPrintF('`ProfileID` = %u',$Profile['ID'])));
if(Is_Error($Count))
	return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
if($Count){
	#-------------------------------------------------------------------------------
	$Contract = DB_Select('Contracts',Array('ID'),Array('UNIQ','Where'=>SPrintF('`ProfileID` = %u',$Profile['ID']),'Limits'=>Array(0,1)));
	#-------------------------------------------------------------------------------
	switch(ValueOf($Contract)){
	case 'error':
        	return ERROR | @Trigger_Error(500);
	case 'exception':
		return ERROR | @Trigger_Error(400);
	case 'array':
		#-------------------------------------------------------------------------------
		$Contract = Comp_Load('Formats/Profile/Number',$Contract['ID']);
		if(Is_Error($Contract))
			return ERROR | @Trigger_Error(500);
		#-------------------------------------------------------------------------------
		break;
		#-------------------------------------------------------------------------------
	default:
		return ERROR | @Trigger_Error(101);
	}
	#-------------------------------------------------------------------------------
	#-------------------------------------------------------------------------------
	$Comp = Comp_Load('Formats/Profile/Number',$Profile['ID']);
	if(Is_Error($Comp))
		return ERROR | @Trigger_Error(500);
	#-------------------------------------------------------------------------------
	return new gException('PROFILE_CAN_NOT_DELETED',SPrintF('Профиль №%s связан с договором №%s и не может быть удален',$Comp,$Contract));
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
// достаём все файлы и удаляем
$Files = GetUploadedFilesInfo('Profiles',$Profile['ID']);
#-------------------------------------------------------------------------------
foreach($Files as $File)
	if(!DeleteUploadedFile($File['ID']))
		return new gException('CANNOT_DELETE_FILE','Не удалось удалить связанный файл');
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return TRUE;
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------

?>
