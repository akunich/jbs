$Result = Array();
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$Where = Array(
		"`Partition` = '/User/Announcement'",
		"`IsPublish` = 'yes'",
		"`IsDOM` = 'yes'",
		"`IsXML` = 'yes'",
	);
#-------------------------------------------------------------------------------
$Clauses = DB_Select('Clauses','Text',Array('Where'=>$Where));
switch(ValueOf($Clauses)){
case 'array':
	#-------------------------------------------------------------------------------
	foreach($Clauses as $Clause){
		$NoBody = new Tag('NOBODY');
		$NoBody->AddHTML($Clause['Text']);
		$Result[] = $NoBody;
		UnSet($NoBody);
	}
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return $Result;
