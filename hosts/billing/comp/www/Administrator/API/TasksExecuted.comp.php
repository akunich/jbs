/******************************************************************************/
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
$Args = Args();
#-------------------------------------------------------------------------------
$TasksIDs = (array) @$Args['RowsIDs'];
#-------------------------------------------------------------------------------
if(Count($TasksIDs) < 1)
  return new gException('TASKS_NOT_SELECTED','Задачи не выбраны');
#-------------------------------------------------------------------------------
$Array = Array();
#-------------------------------------------------------------------------------
foreach($TasksIDs as $TaskID)
  $Array[] = (integer)$TaskID;
#-------------------------------------------------------------------------------
$IsUpdate = DB_Update('Tasks',Array('Errors'=>0,'Result'=>'','IsExecuted'=>TRUE),Array('Where'=>SPrintF('`ID` IN (%s)',Implode(',',$Array))));
if(Is_Error($IsUpdate))
  return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
return Array('Status'=>'Ok');
#-------------------------------------------------------------------------------

?>
