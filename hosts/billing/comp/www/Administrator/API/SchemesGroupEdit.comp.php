Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
if(Is_Error(System_Load('modules/Authorisation.mod')))
  return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
$Args = Args();
#-------------------------------------------------------------------------------
$SchemesGroupID = (integer) @$Args['SchemesGroupID'];
$Name                  =  (string) @$Args['Name'];
#-------------------------------------------------------------------------------
$ISchemesGroup = Array(
  #-----------------------------------------------------------------------------
  'Name' => $Name
);
#-------------------------------------------------------------------------------
$Answer = Array('Status'=>'Ok');
#-------------------------------------------------------------------------------
if($SchemesGroupID){
  #-----------------------------------------------------------------------------
  $IsUpdate = DB_Update('SchemesGroups',$ISchemesGroup,Array('ID'=>$SchemesGroupID));
  if(Is_Error($IsUpdate))
    return ERROR | @Trigger_Error(500);
}else{
  #-----------------------------------------------------------------------------
  $SchemesGroupID = DB_Insert('SchemesGroups',$ISchemesGroup);
  if(Is_Error($SchemesGroupID))
    return ERROR | @Trigger_Error(500);
  #-----------------------------------------------------------------------------
  $Answer['SchemesGroupID'] = $SchemesGroupID;
}
#-------------------------------------------------------------------------------
return $Answer;
#-------------------------------------------------------------------------------

?>
