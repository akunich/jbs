#-------------------------------------------------------------------------------
$Codes = System_XML('tmp/protect.xml');
if(Is_Error($Codes))
  $Codes = Array();
#-------------------------------------------------------------------------------
foreach(Array_Keys($Codes) as $CodeID){
  #-----------------------------------------------------------------------------
  if(Time() - $Codes[$CodeID]['Date'] > 300)
    UnSet($Codes[$CodeID]);
}
#-------------------------------------------------------------------------------
$IsExists = FALSE;
#-------------------------------------------------------------------------------
foreach(Array_Keys($Codes) as $CodeID){
  #-----------------------------------------------------------------------------
  $Code = $Codes[$CodeID];
  #-----------------------------------------------------------------------------
  if($Code['IP'] == $_SERVER['REMOTE_ADDR']){
    #---------------------------------------------------------------------------
    $IsExists = TRUE;
    #---------------------------------------------------------------------------
    $Secret = $Code['Secret'];
    #---------------------------------------------------------------------------
    break;
  }
}
#-------------------------------------------------------------------------------
if(!$IsExists)
  $Codes[UniqID('ID')] = Array('IP'=>$_SERVER['REMOTE_ADDR'],'Secret'=>$Secret = Rand(100000,900000),'Date'=>Time());
#-------------------------------------------------------------------------------
$XML = To_XML_String($Codes);
if(Is_Error($XML))
  return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
$Tmp = System_Element('tmp');
if(Is_Error($Tmp))
  return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
$IsWrite = IO_Write(SPrintF('%s/protect.xml',$Tmp),$XML,TRUE);
if(Is_Error($IsWrite))
  return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
$Path = Styles_Element('Images/ProtectBg.png');
if(Is_Error($Path))
  return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
$Im = ImageCreateFromPng($Path);
#-------------------------------------------------------------------------------
$Color = ImageColorAllocate($Im,0,0,0);
#-------------------------------------------------------------------------------
$Px = (ImageSx($Im) - 8.5 * StrLen($Secret))/2;
#-------------------------------------------------------------------------------
ImageString($Im,5,$Px,9,$Secret,$Color);
#-------------------------------------------------------------------------------
Header('Content-Type: image/png');
ImagePng($Im);
#-------------------------------------------------------------------------------

?>
