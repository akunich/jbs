function &Links(){
  #-----------------------------------------------------------------------------
  $Name = Md5('Links');
  #-----------------------------------------------------------------------------
  if(!IsSet($GLOBALS[$Name]))
    $GLOBALS[$Name] = Array();
  #-----------------------------------------------------------------------------
  return $GLOBALS[$Name];
}
#-------------------------------------------------------------------------------
function &Link_Get($LinkID,$TypeID = NULL){
  #-----------------------------------------------------------------------------
  $Links = &Links();
  #-----------------------------------------------------------------------------
  if(!IsSet($Links[$LinkID])){
    $Links[$LinkID] = NULL;
    #---------------------------------------------------------------------------
    if($TypeID)
      SetType($Links[$LinkID],$TypeID);
  }
  #-----------------------------------------------------------------------------
  return $Links[$LinkID];
}
