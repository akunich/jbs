function &Regulars(){
  #-----------------------------------------------------------------------------
  $Name = Md5('Regulars');
  #-----------------------------------------------------------------------------
  if(!IsSet($GLOBALS[$Name])){
    #---------------------------------------------------------------------------
    $GLOBALS[$Name] = System_XML('config/Regulars.xml');
    if(Is_Error($GLOBALS[$Name]))
      return ERROR | Trigger_Error('[Regulars]: не удалось загрузить файл регулярных выражений');
  }
  #-----------------------------------------------------------------------------
  return $GLOBALS[$Name];
}
