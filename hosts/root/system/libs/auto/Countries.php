function &Countries(){
	#-------------------------------------------------------------------------------
	$Name = Md5('Countries');
	#-------------------------------------------------------------------------------
	if(!IsSet($GLOBALS[$Name])){
		#-------------------------------------------------------------------------------
		$GLOBALS[$Name] = System_XML('config/Countries.xml');
		if(Is_Error($GLOBALS[$Name]))
			return ERROR | Trigger_Error('[Countries]: не удалось загрузить файл с описанием кодов стран');
		#-------------------------------------------------------------------------------
	}
	#-------------------------------------------------------------------------------
	return $GLOBALS[$Name];
	#-------------------------------------------------------------------------------
}
