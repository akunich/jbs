function &Config(){
  #-----------------------------------------------------------------------------
  $Name = Md5('Config');
  #-----------------------------------------------------------------------------
  if(!IsSet($GLOBALS[$Name])){
    #---------------------------------------------------------------------------
    $GLOBALS[$Name] = System_XML('config/Config.xml');
    if(Is_Error($GLOBALS[$Name]))
      return ERROR | Trigger_Error('[Config]: не удалось загрузить основную конфигурацию');
  }
  #-----------------------------------------------------------------------------
  return $GLOBALS[$Name];
}
