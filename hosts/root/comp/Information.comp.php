	#-------------------------------------------------------------------------------
	$Table = new Tag('TABLE',Array('class'=>$TypeID,'cellspacing'=>5,'align'=>'center'),$Tr);
	#-------------------------------------------------------------------------------
	#-------------------------------------------------------------------------------
