$__args_list = Array('Time');
/******************************************************************************/
Eval(COMP_INIT);
/******************************************************************************/
/******************************************************************************/
$Stamp = Abs($Time);
#-------------------------------------------------------------------------------
if(!$Stamp)
  return '-';
#-------------------------------------------------------------------------------
return ($Time < 0?'-':'').
(($Day = (integer)(($Stamp)/86400))?SPrintF(' %u дн.',$Day):
  (($Hour = (integer)(($Stamp - $Day*86400)/3600))?SPrintF(' %u ч.',$Hour):
    (($Minutes = (integer)(($Stamp - $Day*86400 - $Hour*3600)/60))?SPrintF(' %u мин.',$Minutes):
      (($Seconds = $Stamp - $Day*86400 - $Hour*3600 - $Minutes*60)?SPrintF(' %u сек.',$Seconds):'-')
    )
  )
);
#-------------------------------------------------------------------------------

