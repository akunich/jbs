/******************************************************************************/
/******************************************************************************/
$ConfigPath = SPrintF('%s/hosts/%s/config/Config.xml',SYSTEM_PATH,HOST_ID);
#-------------------------------------------------------------------------------
if(File_Exists($ConfigPath)){
	#-------------------------------------------------------------------------------
	$File = IO_Read($ConfigPath);
	if(Is_Error($File))
		return ERROR | @Trigger_Error(500);
	#-------------------------------------------------------------------------------
	$XML = String_XML_Parse($File);
	if(Is_Exception($XML))
		return ERROR | @Trigger_Error(500);
	#-------------------------------------------------------------------------------
	$Config = $XML->ToArray();
	#-------------------------------------------------------------------------------
	$Config = $Config['XML'];
	#-------------------------------------------------------------------------------
}else{
	#-------------------------------------------------------------------------------
	$Config = Array();
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
if(!IsSet($Config['CSRFKey']) || !$Config['CSRFKey']){
	#-------------------------------------------------------------------------------
	$Config['CSRFKey'] = Str_Shuffle(Md5(MicroTime().Rand(0,1000000)));
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
if(IsSet($Config['Interface']['Notes']))
	UnSet($Config['Interface']['Notes']);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
if(IsSet($Config['Other']['Libs']['Http']))
	UnSet($Config['Other']['Libs']['Http']);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
if(IsSet($Config['Users']['Register'])){
	#-------------------------------------------------------------------------------
	$Config['Interface']['User']['Register'] = $Config['Users']['Register'];
	#-------------------------------------------------------------------------------
	UnSet($Config['Users']['Register']);
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
$File = IO_Write($ConfigPath,To_XML_String($Config),TRUE);
if(Is_Error($File))
	return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
$IsFlush = CacheManager::flush();
if(!$IsFlush)
	@Trigger_Error(500);
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return TRUE;
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
?>
