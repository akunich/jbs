$TasksTypes = Array('Email','WhatsApp','SMS','ICQ','Jabber');
#-------------------------------------------------------------------------------
// помечаем задачи как выполненные
foreach($TasksTypes as $TasksType){
	#-------------------------------------------------------------------------------
	$IsUpdate = DB_Update('Tasks',Array('IsExecuted'=>TRUE),Array('Where'=>SPrintF('`TypeID` = "%s"',$TasksType)));
	if(Is_Error($IsUpdate))
		return ERROR | @Trigger_Error(500);
	#-------------------------------------------------------------------------------
}
#-------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
return TRUE;
