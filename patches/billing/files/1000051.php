<?php
#-------------------------------------------------------------------------------
$Path = SPrintF('%s/styles/%s/Images',SYSTEM_PATH,HOST_ID);
#-------------------------------------------------------------------------------
$Sign = SPrintF('%s/Sign.bmp',$Path);
#-------------------------------------------------------------------------------
if(!File_Exists($Sign))
  return TRUE;
#-------------------------------------------------------------------------------
if(!@Copy($Sign,SPrintF('%s/dSign.bmp',$Path)))
  return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
if(!@Copy($Sign,SPrintF('%s/aSign.bmp',$Path)))
  return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
if(!@UnLink($Sign))
  return ERROR | @Trigger_Error(500);
#-------------------------------------------------------------------------------
return TRUE;
#-------------------------------------------------------------------------------
?>