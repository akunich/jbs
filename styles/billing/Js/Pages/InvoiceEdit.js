function InvoiceSumm(){
	//------------------------------------------------------------------------------
	var $Form = document.forms['InvoiceEditForm'];
	//------------------------------------------------------------------------------
	// массив иденфтикаторов
	var $InvoicesItemsIDs = $Form.InvoicesItemsIDs.value.split(',');
	//------------------------------------------------------------------------------
	// сумма всех услуг
	var $Summ = 0;
	//------------------------------------------------------------------------------
	// перебираем идентфикаторы
	for (var i=0;i<$InvoicesItemsIDs.length;i++){
		//------------------------------------------------------------------------------
		// проверяем IsDeletes
		$Key = 'IsDeletes[' + $InvoicesItemsIDs[i] + ']';
		//------------------------------------------------------------------------------
		if($Form[$Key].checked){
			//------------------------------------------------------------------------------
			// ничего не делаем
			//------------------------------------------------------------------------------
		}else{
			//------------------------------------------------------------------------------
			$Key2 = 'ItemSumms[' + $InvoicesItemsIDs[i] + ']';
			//------------------------------------------------------------------------------
			$Summ = Number($Summ) + Number($Form[$Key2].value);
			//------------------------------------------------------------------------------
		}
		//------------------------------------------------------------------------------
		$Form.Summ.value = $Summ;
		//------------------------------------------------------------------------------
	}
	//------------------------------------------------------------------------------
}
