function SettingsUpdate(){
  //----------------------------------------------------------------------------
  var $Form = document.forms['VPSServerEditForm'];
  //----------------------------------------------------------------------------
  var $System = Settings[$Form.SystemID.value];
  //----------------------------------------------------------------------------
  for(var i in $System)
    $Form[i].value = $System[i];
}
