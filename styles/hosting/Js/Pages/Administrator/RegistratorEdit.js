function SettingsUpdate(){
  //----------------------------------------------------------------------------
  var $Form = document.forms['RegistratorEditForm'];
  //----------------------------------------------------------------------------
  var $Type = Settings[$Form.TypeID.value];
  //----------------------------------------------------------------------------
  for(var i in $Type)
    $Form[i].value = $Type[i];
  //----------------------------------------------------------------------------
  $Form['PrefixAPI'].disabled = !$Form['PrefixAPI'].value;
}
